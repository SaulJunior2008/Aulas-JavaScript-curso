/* 
Operadores Lógicos
&& = and/e ->todas as expressões precisam ser verdadeiras para retornar o valor true
|| = or/ou ->uma expressçao verdadeira pode voltar o valor true
! = not/não ->inverte a expressão
*/
const expressãoAND = true && true && true && true;
console.log(expressãoAND);

const expressãoOR = true || false;
console.log(expressãoOR);

const expressãoNOT = !true
console.log(expressãoNOT);